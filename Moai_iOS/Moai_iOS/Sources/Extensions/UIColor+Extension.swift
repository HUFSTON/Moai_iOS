import UIKit

// Colors.xcassets에 있는 색 쉽게 사용하기 위해 extension으로 설정
extension UIColor {
    static let Black1 = UIColor(named: "Black1")!
    static let Charcoal = UIColor(named:"Charcoal")!
    static let DarkGray = UIColor(named:"DarkGray")!
    static let LightGray1 = UIColor(named: "LightGray1")!
    static let LightGray2 = UIColor(named: "LightGray2")!
    static let LightGray3 = UIColor(named: "LightGray3")!
    static let DarkGreen = UIColor(named: "DarkGreen")!
    static let LightGreen = UIColor(named: "LightGreen")!
}
